# Sum using loops
# This program takes a number as input and calculates the sum of all numbers from 1 to that number using a loop.
# It then prints the result.

# Take input from the user
num = int(input("Enter a number: "))

# Initialize the sum variable
sum = 0

# Use a loop to calculate the sum
for i in range(1, num+1):
    sum += i

# Print the result
print("The sum of all numbers from 1 to", num, "is", sum) 
 
# This program is a simple implementation of a loop to calculate the sum of numbers.
# It demonstrates the use of a for loop and the concept of accumulation in programming.
# The program is easy to understand and can be modified to calculate the sum of numbers in different ranges or with different conditions.

